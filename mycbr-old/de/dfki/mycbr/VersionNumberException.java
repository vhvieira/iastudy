/**
 * Thrown if we encounter a version number which is not like [0-9]+\.[0-9]+(\.[0-9]+)*
 * 
 * @author myCBR Team
 * @since v2.6.3
 */
public class VersionNumberException extends RuntimeException {
	private static final long serialVersionUID = 1L;
	
	public VersionNumberException(String msg, Throwable e) {
		super(msg, e);
	}
}


